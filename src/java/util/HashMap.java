    /**
     * Associates the specified value with the specified key in this map.
     * If the map previously contained a mapping for the key, the old
     * value is replaced.
     *
     * @param key key with which the specified value is to be associated
     * @param value value to be associated with the specified key
     * @return the previous value associated with <tt>key</tt>, or
     *         <tt>null</tt> if there was no mapping for <tt>key</tt>.
     *         (A <tt>null</tt> return can also indicate that the map
     *         previously associated <tt>null</tt> with <tt>key</tt>.)
     */
    public V put(K key, V value) {
        return putVal(hash(key), key, value, false, true);
    }

    /**
     * Implements Map.put and related methods.
     *
     * 实现了 map。put 和相关的方法。
     *
     * @param hash hash for key
     * @param key the key
     * @param value the value to put
     * @param onlyIfAbsent if true, don't change existing value 如果为true，则不更改现有值，该方法就变成只为了寻找旧值而已，和getNode的差别就在于 putVal() 会 resize
     * @param evict if false, the table is in creation mode.    如果为false，表示表处于创建模式。
     * @return previous value, or null if none  之前的值，如果没有则为空
     */
    final V putVal(int hash, K key, V value, boolean onlyIfAbsent,
                   boolean evict) {
        Node<K,V>[] tab; Node<K,V> p; int n, i;
        if ((tab = table) == null || (n = tab.length) == 0)
            n = (tab = resize()).length;
        if ((p = tab[i = (n - 1) & hash]) == null)
            // 会出现并发问题
            tab[i] = newNode(hash, key, value, null);
        else {
            Node<K,V> e; K k;
            if (p.hash == hash &&
                ((k = p.key) == key || (key != null && key.equals(k))))
                e = p;
            else if (p instanceof TreeNode)
                e = ((TreeNode<K,V>)p).putTreeVal(this, tab, hash, key, value);
            else {
                for (int binCount = 0; ; ++binCount) {
                    if ((e = p.next) == null) {
                        p.next = newNode(hash, key, value, null);
                        if (binCount >= TREEIFY_THRESHOLD - 1) // -1 for 1st
                            // 开始转成树容器
                            treeifyBin(tab, hash);
                        break;
                    }
                    if (e.hash == hash &&
                        ((k = e.key) == key || (key != null && key.equals(k))))
                        break;
                    p = e;
                }
            }
            if (e != null) { // existing mapping for key    现有的 key 映射
                V oldValue = e.value;
                if (!onlyIfAbsent || oldValue == null)
                    e.value = value;
                afterNodeAccess(e);
                return oldValue;
            }
        }
        // 记录操作次数+1。  会出现并发问题
        ++modCount;
        // 会出现并发问题
        if (++size > threshold)
            resize();

        // 钩子函数
        afterNodeInsertion(evict);
        return null;
    }

    /**
     * Initializes or doubles table size.
     * 初始化或者加倍 table 大小。
     * If null, allocates in accord with initial capacity target held in field threshold.
     * 如果为空，则按照字段阈值中持有的初始容量目标进行分配。
     * Otherwise, because we are using power-of-two expansion,
     * 否则，因为我们用的是2的幂展开
     * the elements from each bin must either stay at same index,
     * or move with a power of two offset in the new table.
     * 每个容器中的元素必须保持相同的索引，或者在新表中以2的幂移动偏移量
     * @return the table
     */
    final Node<K,V>[] resize() {
        // 老的 tab
        Node<K,V>[] oldTab = table;
        // 老的 tab length
        int oldCap = (oldTab == null) ? 0 : oldTab.length;
        // 本次扩容的容量
        int oldThr = threshold;
        // 新 tab length, 下一次需要扩容的容量
        int newCap, newThr = 0;

        // >>>>>>>>>>>>> 这部分代码只是用来计算下次需要扩展的容量 >>>>>>>>>>>>>>>>

        /*
        * 1.如果 table 不等 null 并且长度大于 0
        *   1.如果已经已经是最大容量的，就不再扩充了 return
        *   2.如果当前 table 长度大于默认初始化的长度，table 先计算下一次需要扩充的容量，先暂时记录在 newThr ，值为这次 threshold << 1 就是左移1位
        * 2.如果这次扩容的容量是大于 0，那就先在 newCap 记录着
        * 3.如果 table 等于 null 或者 长度为 0 就代表只是做初始化：newCap 为初始化值 16，newThr 也是初始化的下次扩充的值 (int)(0.75 * newCap)
        * 4.如果下次要扩充的值为0，那就用到 loadFactor 了，计算 newThr  newCap * loadFactor TODO 不知道什么时候会走到这个分支，后面再看看
        * 下次要扩充的值就已经算好了
        */
        if (oldCap > 0) {
            // 最大容量 1 << 30
            if (oldCap >= MAXIMUM_CAPACITY) {
                // 下次最大容量就是int 最大值
                threshold = Integer.MAX_VALUE;
                return oldTab;
            }
            else if ((newCap = oldCap << 1) < MAXIMUM_CAPACITY &&
                     oldCap >= DEFAULT_INITIAL_CAPACITY)
                // 下一次需要扩充的容量为 threshold << 1 左移1位
                newThr = oldThr << 1; // double threshold 双倍 threshold
        }
        else if (oldThr > 0) // initial capacity was placed in threshold    初始容量设置为阈值
            newCap = oldThr;
        else {               // zero initial threshold signifies using defaults 零初始阈值表示使用默认值
            newCap = DEFAULT_INITIAL_CAPACITY;
            newThr = (int)(DEFAULT_LOAD_FACTOR * DEFAULT_INITIAL_CAPACITY);
        }
        if (newThr == 0) {
            float ft = (float)newCap * loadFactor;
            newThr = (newCap < MAXIMUM_CAPACITY && ft < (float)MAXIMUM_CAPACITY ?
                      (int)ft : Integer.MAX_VALUE);
        }
        threshold = newThr;
        // <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<

        // >>>>>>>>>>>>>>>>> 开始扩容 >>>>>>>>>>>>>>>>>>

        @SuppressWarnings({"rawtypes","unchecked"})
        // newCap = oldCap << 1 原本数量左移了一位
        Node<K,V>[] newTab = (Node<K,V>[])new Node[newCap];
        table = newTab;
        if (oldTab != null) {
            for (int j = 0; j < oldCap; ++j) {
                Node<K,V> e;
                if ((e = oldTab[j]) != null) {
                    // 断开引用
                    oldTab[j] = null;
                    // 如果e 没有下个节点，重新hash然后找位置
                    if (e.next == null)
                        newTab[e.hash & (newCap - 1)] = e;

                    // TODO split方法是树的重新找位置方法？
                    else if (e instanceof TreeNode)
                        ((TreeNode<K,V>)e).split(this, newTab, j, oldCap);
                    else { // preserve order 维持秩序
                        /* 桶分割
                         * 分两组 1. loHead、loTail
                         *       2. hiHead、hiTail
                         *       原先的结构：loHead -> loTail -> hiHead -> hiTail
                         *       会将 loTail -> hiHead 拆掉
                         *       变成 [j] = loHead -> loTail  (==0)
                         *           [j + oldCap] = hiHead -> hiTail    (!=0)
                         *
                         * 为什么要用桶分割，因为这种算法很神奇，就是能分割的正确。
                         * 原理为二进制计算
                         * 1.HashMap 初始化长度n为 16，n 二进制为 10000，n-1 二进制为 1111
                         *      两个key，key1.hash = 50，key2.hash = 82，  二进制分别为 0110010，1010010
                         *      根据计算 (hash & n-1),   也就是：1111
                         *                                 0110010 -> 0010 = 2
                         *                                 1010010 -> 0010 = 2
                         *      这两个key就是在下标[2]
                         * 2.扩容，n 长度为 32，n 二进制为 100000，n-1 二进制为 11111
                         *      根据计算 (hash & oldn) == 0 ? 停留原下标 : 原下标+oldn
                         *      oldn 二进制为：10000
                         *                 0110010 -> 10000  -> != 0 : = 2+16 = 18
                         *                 1010010 -> 10000  -> != 0 : = 2+16 = 18
                         *      这两个key就是这下标[18]
                         *      进行验算一下：
                         *      根据计算 (hash & n-1),   也就是：11111
                         *                                  0110010 -> 10010 = 18
                         *                                  1010010 -> 10010 = 18
                         *      验算成功，就是在 [18]
                         * 3.再扩容，n 长度为 64，n 二进制为 1000000，n-1 二进制为 111111
                         *      根据计算 (hash & oldn) == 0 ?停留原下标 : 原下标+old
                         *      oldn 二进制为：100000
                         *                  0110010 -> 100000 -> !=0 : = 18+32 = 50
                         *                  1010010 -> 000000 -> ==0 : = 18+0  = 18
                         *      这两个key下标分别为50，18
                         *      进行验算一下：
                         *      根据计算 (hash & n-1)，  也就是：111111
                         *                                   0110010 -> 110010 = 50
                         *                                   1010010 -> 010010 = 18
                         *      验算成功，就是在 [50]，[18]
                         *
                         */
                        Node<K,V> loHead = null, loTail = null;
                        Node<K,V> hiHead = null, hiTail = null;
                        Node<K,V> next;
                        do {
                            next = e.next;
                            if ((e.hash & oldCap) == 0) {
                                if (loTail == null)
                                    // 获取头部
                                    loHead = e;
                                else
                                    // 获取头部下的链表结构Node
                                    loTail.next = e;
                                loTail = e;
                            }
                            else {
                                if (hiTail == null)
                                    // 获取头部
                                    hiHead = e;
                                else
                                    // 获取头部下的链表结构Node
                                    hiTail.next = e;
                                hiTail = e;
                            }
                        } while ((e = next) != null);
                        if (loTail != null) {
                            // 一个链表断开成两个
                            loTail.next = null;
                            newTab[j] = loHead;
                        }
                        if (hiTail != null) {
                            // 断开
                            hiTail.next = null;
                            newTab[j + oldCap] = hiHead;
                        }
                    }
                }
            }
        }
        return newTab;
    }

    /**
     * Replaces all linked nodes in bin at index for given hash unless
     * table is too small, in which case resizes instead.
     *
     * 替换给定哈希的容器中所有的链接节点，除非表太小，在这种情况下改为调整大小。
     *
     * 转成树容器
     */
    final void treeifyBin(Node<K,V>[] tab, int hash) {
        int n, index; Node<K,V> e;
        if (tab == null || (n = tab.length) < MIN_TREEIFY_CAPACITY)
            resize();
        else if ((e = tab[index = (n - 1) & hash]) != null) {
            // hd = head头  tl = tail尾
            TreeNode<K,V> hd = null, tl = null;
            do {
                // new 一个新的 TreeNode 变成树节点
                TreeNode<K,V> p = replacementTreeNode(e, null);
                if (tl == null)
                    hd = p;
                else {
                    p.prev = tl;
                    tl.next = p;
                }
                tl = p;
            } while ((e = e.next) != null);
            if ((tab[index] = hd) != null)
                hd.treeify(tab);
        }
    }

